let number = 7;

if (number <= 2) {
  console.log("Not A Shape");
} else if (number == 3) {
  console.log("It's a triangle");
} else if (number == 4) {
  console.log("It's a quadilateral");
} else if (number == 5) {
  console.log("It's a Pentagon");
} else if (number == 6) {
  console.log("It's a Hexagon");
} else if (number == 7) {
  console.log("It's a Heptagon");
} else if (number == 8) {
  console.log("It's a Octagon");
} else if (number == 9) {
  console.log("It's a Nonagon");
} else if (number == 10) {
  console.log("It's a Decagon");
} else {
  console.log("NUMBER SHOULD NOT EXCEED 10");
}
